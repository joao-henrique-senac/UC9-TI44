package execicios.Atividades;

public class exer8 {
//    ## Exercício 8 - Converter Milhas em Kilometros
//Faca um algoritmo para transformar uma distância expressa em milhas para quilômetros.
//Sabe-se que uma milha corresponde a 0,6214 km.(usar ponto como separador)


    public static void main(String[] args) {
        double milhas = 10.0; // Distância em milhas

        double kilometros = milhas * 0.6214; // Cálculo da distância em quilômetros

        System.out.println(milhas + " milhas correspondem a " + kilometros + " quilômetros.");
    }
}
